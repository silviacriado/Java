package M1.nivel2;

public class cast {
    public static void main(String[] args) {
        double d = 3.1415;
        int i = (int) d;
        System.out.println(d);
        System.out.println(i);

        float f = (float) d;
        System.out.println(f);

        String s = String.valueOf(d);
        System.out.println(s);
    }
}
